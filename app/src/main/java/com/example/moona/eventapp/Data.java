public class Data {

    private String mName;
    private String mDate;

    public Data(String mName, String mDate)
    {
        this.mName = mName;
        this.mDate = mDate;
    }

    public String getmName() {
        return mName;
    }

    public String getmDate() {
        return mDate;
    }

}
